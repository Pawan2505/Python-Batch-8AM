a = 5   # 101 in binary
b = 3   # 011 in binary

print("Bitwise AND:", a & b)  # 1 (001)
print("Bitwise OR:", a | b)   # 7 (111)
print("Bitwise XOR:", a ^ b)  # 6 (110)
print("Bitwise NOT:", ~a)     # -6 (inverts bits)
print("Left Shift:", a << 1)  # 10 (1010)
print("Right Shift:", a >> 1) # 2 (10)
