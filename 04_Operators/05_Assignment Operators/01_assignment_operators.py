x = 5
print("Initial value:", x)

x += 3  # x = x + 3
print("After +=:", x)

x -= 2  # x = x - 2
print("After -=:", x)

x *= 2  # x = x * 2
print("After *=:", x)

x /= 3  # x = x / 3
print("After /=:", x)

x //= 2  # x = x // 2
print("After //=:", x)

x %= 3  # x = x % 3
print("After %=:", x)

x **= 2  # x = x ** 2
print("After **=:", x)
